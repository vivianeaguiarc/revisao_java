package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		double a = 4.5;
		var b = 5.5;
		
		System.out.println(a);
		System.out.println(b);
		
		var c = "Hello";
		System.out.println(c);
		c = "Hello World";
		System.out.println(c);
		
		double d;
		d = 123.45;
		System.out.println(d);
	}
}
